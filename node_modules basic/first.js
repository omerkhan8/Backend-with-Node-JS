module.exports = (x, y) => {
    return new Promise((resolve, reject) => {
        if (x <= 0 || y <= 0) {
            setTimeout(() => {
                reject(new Error(`Dimensions should be greater than 0, l : ${x}, b : ${y}`));
            }, 2000)
        }
        else {
            setTimeout(() => {
                resolve({
                    perimeter: () => (2 * (x + y)),
                    area: () => (x * y)
                });
            }, 2000)
        }
    })


}